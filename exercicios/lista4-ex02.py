# 2. Escreva um algoritmo para calcular a área de um trapézio

basemaior = int(input("Digite o valor da base maior: "))
basemenor = int(input("Digite o valor da base menor: "))
altura = int(input("Digite o valor da altura: "))

trapezio = ((basemaior + basemenor) * altura) / 2
print("A área do trapézio é", trapezio)
