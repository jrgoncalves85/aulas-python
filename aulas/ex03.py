print ("Operadores aritméticos")
print (5+2)     # adição
print (5-2)     # subtração
print (5*2)     # multiplicação
print (5/2)     # divisão
print (5**2)    # potenciação
print (5//2)    # divisão inteira
print (5%2)     # resto de divisão

print ("Operadores relacionais")
print (5 > 2)     # maior que
print (5 < 2)     # menor que
print (5 >= 2)     # maior e igual a 
print (5 <= 2)     # menor e igual a 
print (5 == 2)     # é igual a 
print (5 != 2)     # é diferente de

print ("Precedência dos operadores")
print ("1 - parenteses\n2 - potenciação\n3 - multiplicação e divisão \n4 - adição e subtração ")
print (5 + 2 / 2)
print ((5+2) / 2)

