# 9. Escreva um algoritmo para receber um número. # Se o número for
# maior que 100, deve-se exibir a mensagem “Número maior que 100!”.
# Caso contrário, exiba a mensagem “Programa encerrado”.

num = int(input("Digite um número:"))
if num > 100:
    print("Número maior que 100!")
else:
    print("Programa encerrado")
