# 3. Escreva um algoritmo que leia três números nas variáveis Val1, Val2 e Val3, 
# calcule sua média na variável media e exiba para o usuário o resultado.

val1 = int(input("Digite o valor 1: "))
val2 = int(input("Digite o valor 2: "))
val3 = int(input("Digite o valor 3: "))

media = (val1+val2+val3) / 3
print("A média dos números é", media)
